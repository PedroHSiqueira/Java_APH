package com.mycompany.repeticoes;

public class Repeticoes {

    public static void main(String[] args) {
        // Do While

        int contCambalhota = 1;

        do {
            System.out.println("Cambalhota: " + contCambalhota);
            contCambalhota++;
        } while (contCambalhota <= 5);
    }
}
